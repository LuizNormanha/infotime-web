CREATE TABLE IF NOT EXISTS public.infotime_cliente_comportamento_pagamento (
  id_cliente_comportamento int8 GENERATED BY DEFAULT AS IDENTITY (
    INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1 NO CYCLE
  ) NOT NULL,
  id_tenacidade int8 NOT NULL,
  id_cliente int8 NOT NULL,
  id_usuario_auditoria int8 NULL,
  dias_media_atraso numeric(6, 2) NULL,
  dias_desvio_padrao numeric(6, 2) NULL,
  percentual_pagamento_dia numeric(5, 2) NULL,
  percentual_ate_7_dias numeric(5, 2) NULL,
  percentual_ate_30_dias numeric(5, 2) NULL,
  percentual_acima_30_dias numeric(5, 2) NULL,
  ticket_medio_mensal numeric(15, 2) NULL,
  meses_analisados int4 NULL,
  data_calculo timestamptz NOT NULL DEFAULT now(),
  endereco_ip_auditoria varchar(50) NULL,
  nome_aplicacao_auditoria varchar(255) NULL,
  CONSTRAINT infotime_cli_comp_pkey PRIMARY KEY (id_cliente_comportamento),
  CONSTRAINT fk_infotime_ccp_ten FOREIGN KEY (id_tenacidade)
    REFERENCES public.infotime_tenacidade (id_tenacidade) ON DELETE RESTRICT ON UPDATE RESTRICT,
  CONSTRAINT fk_infotime_ccp_cli FOREIGN KEY (id_cliente)
    REFERENCES public.infotime_cliente (id_cliente) ON DELETE RESTRICT ON UPDATE RESTRICT,
  CONSTRAINT fk_infotime_ccp_ua FOREIGN KEY (id_usuario_auditoria)
    REFERENCES public.infotime_usuario (id_usuario) ON DELETE RESTRICT ON UPDATE RESTRICT,
  CONSTRAINT uq_infotime_ccp UNIQUE (id_tenacidade, id_cliente)
);

